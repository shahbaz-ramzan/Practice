// 37.Reverse: Write a function that takes an array of strings and returns a new array with the strings in reverse order.
// Example Input: ['apple', 'banana', 'cherry']
// Expected Output: ['cherry', 'banana', 'apple']


let array=["apple", "banana", "cherry"]

function reverse(array){
    return array.reverse()
}

let result=reverse(array)

